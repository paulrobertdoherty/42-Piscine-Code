void	ft_print_comb(void)
{
	char a[3];

	a[0] = 0;
	while (a[0] <= 7)
	{
		a[1] = a[0] + 1;
		while (a[1] <= 8)
		{
			a[2] = a[1] + 1;
			while (a[2] <= 9)
			{
				ft_putchar(a[0] + 48);
				ft_putchar(a[1] + 48);
				ft_putchar(a[2] + 48);
				if (a[0] != 7 || a[1] != 8 || a[2] != 9)
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
				a[2] = a[2] + 1;
			}
			a[1] = a[1] + 1;
		}
		a[0] = a[0] + 1;
	}
}
