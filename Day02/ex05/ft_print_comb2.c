void	ft_print_comb2(void)
{
	char a[2];

	a[0] = 0;
	while (a[0] <= 98)
	{
		a[1] = a[0] + 1;
		while (a[1] <= 99)
		{
			ft_putchar((a[0] / 10) + 48);
			ft_putchar((a[0] % 10) + 48);
			ft_putchar(' ');
			ft_putchar((a[1] / 10) + 48);
			ft_putchar((a[1] % 10) + 48);
			if (a[0] != 98 || a[1] != 99)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			a[1] = a[1] + 1;
		}
		a[0] = a[0] + 1;
	}
}
