void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	if (str[0])
	{
		ft_putchar(str[0]);
		ft_putstr(&str[1]);
	}
}

void	ft_print_words_tables(char **tab)
{
	int i;

	i = 0;
	while (tab[i])
	{
		ft_putstr(tab[i]);
		ft_putchar('\n');
		i++;
	}
}
