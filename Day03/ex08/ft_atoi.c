int	ft_atoi(char *str)
{
	int tr;
	int i;
	int negative;

	tr = 0;
	i = 0;
	negative = 0;
	if (str[0] != 0 && str[0] == '-')
	{
		negative = 1;
		i = 1;
	}
	while (str[i] != 0)
	{
		if (negative == 0)
		{
			tr = (tr * 10) + (str[i] - 48);
		}
		else
		{
			tr = (tr * 10) - (str[i] - 48);
		}
		i = i + 1;
	}
	return (tr);
}
